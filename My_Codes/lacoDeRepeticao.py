#For
# Em Python o For age como um iterador. Ele passa por itens que estão em uma sequência ou qualquer outro itém iterável
# Sintaxe:
# for item in objeto:
#      fazer algo

l = [1,2,3,4,5,6,7]
print(l)

for num in l:
  print(num)

for num in l:
  print('Num vezes 1:', num)
  print('Num vezes 2:', num * 2)
  print('Num vezes 3:', num * 3)
  